CREATE TABLE ncart_pubs (ID int, 
                         title varchar(400), 
                         authors varchar(800), 
                         publisher varchar(250), 
                         month varchar(10), 
                         year int, 
                         vol int, 
                         pg_start int, 
                         pg_end int, 
                         abstract mediumtext, 
                         url varchar(250), 
                         img_url varchar(250), 
                         tags varchar(500), 
                         city varchar(40), 
                         country varchar(40), 
                         project_url varchar(300), 
                         issue int, 
                         video_url varchar(400), 
                         article int);
